const solvePart1 = (seatList) => {
  let row;
  let column;
  const seatIds = [];

  seatList.forEach((seat) => {
    row = seat.substr(0,7).replace(/b/ig, '1').replace(/f/ig, '0');
    column = seat.substr(7).replace(/r/ig, '1').replace(/l/ig, '0');

    seatIds.push((parseInt(row, 2) * 8 + parseInt(column, 2)))
  })

  console.log('Answer part 1: ', Math.max(...seatIds))
  return seatIds
};


const seatIds = solvePart1(seatList)

/*
* --- Part Two ---

Ding! The "fasten seat belt" signs have turned on. Time to find your seat.

It's a completely full flight, so your seat should be the only missing boarding pass in your list. However, there's a catch: some of the seats at the very front and back of the plane don't exist on this aircraft, so they'll be missing from your list as well.

Your seat wasn't at the very front or back, though; the seats with IDs +1 and -1 from yours will be in your list.

What is the ID of your seat?
*/

const solvePart2 = (seatIds) => {
  seatIds = seatIds.sort((a, b) => a - b)
  for (let i = 0, len = seatIds.length; i < len; i++) {
    if (seatIds[i + 1] !== seatIds[i] + 1) {
      return seatIds[i] + 1;
    }
  }
};

